package cn.jbit.myshopping;

public class case2exercise {
	public static void main(String[] args) {
		int leftbefore=10;
		int rightbefore=8;
		int leftafter=rightbefore;
		int rightafter=leftbefore;
		System.out.println("输出互换前手中的纸牌：");
		System.out.println("左手中的纸牌："+leftbefore);
		System.out.println("右手中的纸牌："+rightbefore);
		System.out.println("\n");
		System.out.println("输出互换后手中的纸牌：");
		System.out.println("左手中的纸牌："+leftafter);
		System.out.println("右手中的纸牌："+rightafter);
	}

}
